package com.will.team4final.member.model;

import java.sql.Timestamp;

public class MemberVO {
	private int memberNo;
	private String userid;
	private String pwd;
	private String name;
	private String imageURL;
	private String nickname;
	private String birth;
	private int gender;
	private String zipcode;
	private String address;
	private String addressDetail;
	private String email;
	private String hp;
	private Timestamp regdate;
	private Timestamp outdate;
	public int getMemberNo() {
		return memberNo;
	}
	public void setMemberNo(int memberNo) {
		this.memberNo = memberNo;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getImageURL() {
		return imageURL;
	}
	public void setImageURL(String imageURL) {
		this.imageURL = imageURL;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public String getBirth() {
		return birth;
	}
	public void setBirth(String birth) {
		this.birth = birth;
	}
	public int getGender() {
		return gender;
	}
	public void setGender(int gender) {
		this.gender = gender;
	}
	public String getZipcode() {
		return zipcode;
	}
	public void setZipcode(String zipcode) {
		this.zipcode = zipcode;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getAddressDetail() {
		return addressDetail;
	}
	public void setAddressDetail(String addressDetail) {
		this.addressDetail = addressDetail;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getHp() {
		return hp;
	}
	public void setHp(String hp) {
		this.hp = hp;
	}
	public Timestamp getRegdate() {
		return regdate;
	}
	public void setRegdate(Timestamp regdate) {
		this.regdate = regdate;
	}
	public Timestamp getOutdate() {
		return outdate;
	}
	public void setOutdate(Timestamp outdate) {
		this.outdate = outdate;
	}
	@Override
	public String toString() {
		return "MemberVO [memberNo=" + memberNo + ", userid=" + userid + ", pwd=" + pwd + ", name=" + name
				+ ", imageURL=" + imageURL + ", nickname=" + nickname + ", birth=" + birth + ", gender=" + gender
				+ ", zipcode=" + zipcode + ", address=" + address + ", addressDetail=" + addressDetail + ", email="
				+ email + ", hp=" + hp + ", regdate=" + regdate + ", outdate=" + outdate + "]";
	}
	
	
}
